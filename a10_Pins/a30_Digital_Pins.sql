
-- High (1) / Low (0)

  # In Arduino,
    + High = ~ 3.5V - 5V
    + Low  = ~   0V - 3.0V

-- Digital Pins

  # Large Header

    + 0 - 13
      - Can be used as Digital I/O pins

  # Small Header
    + A0 - A5
      - Can be used as both
        . Analog Input Pins or
        . Digital I/O pins


